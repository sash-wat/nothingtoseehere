        "*** YOUR CODE HERE ***"
        #Go through all iterations
        while(self.iterations != 0):
            #Create temporary dictionary to store data, so that all states can use old value information
            temp_dict = util.Counter()
            #Go through every state in MDP according to Value iteration formula
            for state in self.mdp.getStates():
                if self.mdp.isTerminal(state):
                    continue
                curr_max = -9999999999999
                #Go through every action for the states
                for action in self.mdp.getPossibleActions(state):
                    qvalue = 0
                    #Calculate value of each action from that state
                    for transition, prob in self.mdp.getTransitionStatesAndProbs(state, action):
                        reward = self.mdp.getReward(state, action, transition)
                        qvalue += prob * (reward + (self.discount*self.getValue(transition)))
                    #If value is greater than current value, swap
                    if qvalue > curr_max:
                        curr_max = qvalue
                #Update the Counter dictionary
                temp_dict[state] = curr_max
            #Update the overall dictionary using the new values
            self.values = temp_dict
            self.iterations = self.iterations - 1
